                    <p style="text-align: justify; font-size: 16px" >
                    <br>
                    Usai pelaksanaan OPEN MM ke-1 pengurus segera menggelar musyawarah akbar dalam rangka pembentukan pengurus baru untuk periode tahun ke-3. Dari musyawarah tersebut terpilihlan Zul Fani sebagai ketua majelis Mahabbaturrasul dari pihak laki-laki dan Eka Wahyuni sebagai ketua majelis di pihak perempuan. Pasca pembentukan pengurus yang terpisah berdasarkan jenis kelamin ini, majelis Nampak semakin berkembang pesat. Anggota yang bergabung dari awal cukup membeludak sehingga hanya dalam bulan pertama latihan gendang dan sholawat pun harus dilakukan dengan mengatur jadwal penggunaan gendang. Hal ini dilakukan agar menghindari konflik antar grup dalam penggunaan gendang. Latihan di era ini sudah mulai dengan jadwal yang teratur disertai pelatihnya, adanya laporan dokumentasi pelaksaan latihan serta absensinya. Adapun pelaksaan wirid pekanan tetap berlangsung tanpa hambatan yang berarti bahkan semakin eksis sebab bersamaan dengan momen kajiannya. Sesekali pada momen wirid dan kajian tersebut pengurus melakukan pengaturan jadwal penampilan sesuai grup-grup yang telah ada. Hal ini sangat sukses menarik minat serta meningkatkan skill dan kesiapan penampilan untuk publik. 
                    <br><br>
                    OPEN MM ke-2 terlaksana dengan sukses di era kepengurusan Zul Fani dkk. Di masa ini terjadi juga penambahan dua grup baru. Satu grup dari laki-laki diberi nama Nurussalam dan satu grup dari perempuan bernama Urwatil Wutsqo. Di momen OPEN MM ke-2 ini lomba ditekankan pada penguasaan baca rawi maulid, mahallul qiyam, sholawat dan tabuhan gendang. Skill yang berupa penguasaan bidang tertentu sudah diberi porsi khusus untuk dinilai sehingga terpilihlah munsyid (vocal) terbaik, gendang perasuk terbaik, peningkah terbaik, penggulung terbaik, bass terbaik, serta darbuka terbaik. Hal ini menjadi satu tonggak awal perkembangan majelis di bidang skill dan seni yang berkualitas.
                    <br><br>
                    Era kepengurusan Zul Fani telah mampu juga mendobrak keterlibat majelis berbagai perhelatan acara di tingkat kecamatan. Perkembangan dan perluasan medan dakwah majelis mahabbaturrasul telah terbuka luas dan dikenal masyarakat sehingga undangan berbagai hajatan di masyarakat makin banyak. Apalagi usai perekrutan anggota baru majelis yang kemudian menjadi estafet awal puncak perkembangan majelis Mahabbaturrasul. 
                    <br><br>
                    Perkembangan dakwah majelis mahabbaturrasul erat pula hubungannya pada peran kedatangan para habaib yang kerap berdakwah ke Tanah Merah dan Kuala Enok. Ada berpuluh orang habaib yang berkesempatan hadir dan majelis mahabbaturrasul diberi kepercayaan untuk mengiring sholawat pembuka majelis serta pembacaan maulid. Seorang habib dari Palembang bernama Habib Agil bin AbdulQadir Baraghbah justru sempat memberikan ijazahan ulang maulid Adh-dhyaullami kepada pembina. Hal ini dimaksudkan guna menjalin sanad ilmu dan amal ke kalangan zuriat Rasulullah Saw serta mendekatkan sanad/ijazah karena habib Agil adalah murid langsung Habib Umar bin Hafidz (Pengarang Maulid Adh-dhyaullami). Pada satu kesempatan, habib Agil bahkan menyempatkan berkunjung dan mengisi tausiyah di MAN Kuala Enok yang di masa itu telah berganti nama menjadi MAN 2 Indragiri Hilir. Kehadiran habib Hamid Nagib BSA (Pimpinan Ponpes Al Khairat Bekasi) dalam beberapa momen juga sangat mempengaruhi keberkahanan dakwah mejelis. Pada suatu kesempatan bahkan sengaja keluarga besar H. Kharil Anwar (alm) mengundang majelis dalam rangka memeriahkan tausiyah yang diisi oleh habib Hamid Nagib BSA. 
                    <br><br>
                    Agenda rutinan yang telah menjadi program utama terus berjalan dengan baik. Dengan berbagai tantangannya, agenda-agenda tersebut kemudian membentuk satu citra positif dan budaya khas bagi segenap anggota majelis. Peran bimbingan dan latihan berjenjang sesuai garis tingkatan kelas dan grup telah memberi kesan tersendiri bagi anggota majelis. Perkembangan ini kemudian menjelma menjadi keinginan kuat pembina dan pengurus untuk meletakkan dasar-dasar perluasan medan dakwahnya dengan menjelma menjadi bagian yang tak terpisahkan dari masyarakat umum. Hal ini lah yang kemudian menjadi embrio cikal bakal lahirnya ‘Muhibbin Umum’ Majelis Mahabbaturrasul. Pemikiran ini terkuak oleh kenyataan dari banyaknya peluang anggota baru lintas sekolah dan pemuda. Mereka awalnya tertarik karena eksistensi majelis mahabbaturrasul pada kajian pekanan di masjid besar yang mampu memikat kaula muda untuk duduk mendalami Islam. Di samping itu, sebagian lagi justru sangat tertarik pada keterampilan tabuhan gendang dan senandung sholawat yang menyejukkan qalbu. Di masa ini, ketua majelis untuk laki-laki dan perempuan merupakan Munsyid unggulan majelis. Gerak dua figur ini telah mampu mengokohkan dasar-dasar genre baru sholawat untuk ditampilkan. Terlebih lagi pasca kehadiran munsyid-munsyid baru yang cukup berbakat. Adapun nama-nama yang banyak berperan di era ini antara lain : Zul Fani, Ilham Maulana, Furqan Rifa’I, Adrian Maulana, Teguh Maholtra, Faturrahman, Suhendri Prayoga, Abdul Rasyid, Wahyudin, Muhammad Awal Syam, M. Nasir, M. Jamil, dan Andik. Sementara untuk perempuannya ada nama Eka Wahyuni, Gayuh Puji Rahayu, Miranda Sesby, Nurhasanah, Tuty Alawiyah, Aulia Ramadhani, Riska Nuratul Hikmah, Dena Rahmailahi, Mahyanatul Muthmainnah, Mutiara Haryana Putri, Siti Kholijah Harahap, Marwah dan Nada Amalia.
                    <br><br>
                    Setahun berlalu, kepengurusan pimpinan Zul Fani dan Eka pun digantikan oleh M. Firmansyah. Di era ini, majelis makin menuju puncak kejayaan dakwahnya di bidang maulid, sholawat dan tabuhan gendang. Even-even undangan di masyarakat semakin meningkat. Hal ini bermakna semakin banyaknya ketertarikan dan rasa penerimaan masyarakat akan syiar sholawat. Anggota dari kalangan Muhibbin pun makin menunjukkan peran nyata mereka. Kajian umum pekanan mulai dihadiri oleh ratusan jamaah yang hadir dari lintas umur, dan umumnya dari kalangan pemuda dan pelajar. Latihan gendang dan sholawat tetap terjadwal sesuai giliran grup-grup. Beberapa ukiran sejarah yang menjadi tonggak kepercayaan publik terwujud nyata juga di masa ini. Diantara yang terbilang fenomenal ialah dipercayakan mengiring sholawat dan maulid pada momen tabligh akbar yang diisi oleh Ustadz Abdul Shomad di Desa Tanah Merah dan Kel. Kuala Enok. Tercatat ada beberapa penceramah Nasional dan Lintas Provinsi yang sempat hadir dan majelis mahabbaturrasul turut dipercayakan mengiring sholawatan untuk momen acaranya tersebut. Dalam catatan tersebut nama Ustadz Zulkifli Muhammad Ali, Ustadz Orange, Ustadz Maruli Hasibuan, Ustadz Novri Naldi Safni, KH. Ilyas Marwal, Ustadz Fakhurrazi K. Tungkal, Ustadz H. Anwar Ardabili, Ustadz H. Fakhuruddin Noor, dll. 
                    <br><br>
                    Perkembangan majelis di masa ini juga terkait erat dengan figur-figur pengurusnya yang berbakat dan punya andil besar berjuang untuk majelis. Di lini tarik suara (munsyid) ada nama Ihwal Pat Pratama, dan Sidiq Jalu. Dua munsyid ini telah terkoneksi dengan majelis mahabbaturrasul sejak mereka masih di bangku SMP. Sejak masuk di MAN 2 Indragiri Hilir, mereka banyak mewaranai majelis dengan berbagai performance dan petualangan genre sholawat dan qasidah baru. Sholawat dan Lagu hasil besutan grup Syubanul Muslimin, Majelis Rasulullah, Az-Zahir dll telah mampu ditampilkan hingga memikat publik pecinta sholawat di Tanah Merah dan Kuala Enok. Tidak hanya itu, beberapa anggota yang memiliki bakat serta skill yang amat baik banyak belajar kepada Hadroh Riyadhatusholihin – Pekanbaru dalam beberapa pertemuan terbatas. Grup Hadroh yang kini dimanagemeni oleh M. Yunus (Pendahulu Majelis Mahabbaturrasul) telah dikenal publik Pekanbaru bahkan Riau dan manca negara. Maka tentu hal ini juga menjadi satu hal yang positif bagi ruh perkembangan syiar sholawat dan tabuhan gendang di MM.
                    <br><br>
                    Pada OPEN MM ke-3 yang diikuti oleh 9 grup, tampak perkembangan skill dan performance peserta yang sudah lumayan meningkat. Pada momen tersebut, panitia menghadirkan dua orang pentolan utama dari grup RS (Riyadhatusholihin) yakni M. Yunus yang terlibat sebagai Juri, serta M. Alimin yang dimintakan ikut menilai secara umum serta turut andil berbagi pengalamannya seputar gendang wabil khusus gendang darbuka. Banyak hal positif yang didapat dari momen OPEN MM ke-3 dan workshop gendang oleh Hadroh RS bagi majelis Mahabbaturrasul. Hal ini membawa pula pada versi baru penampilan gendang yang dibawakan oleh majelis. Angin sejuk ini seolah telah mereformasi sisi skill tabuhan gendang di majelis. Di samping itu pula, di era ini ada beberapa hal baru yang tercipta guna meningkatkan skill, wawasan serta keTaqwaan bagi masing-masing anggota. Melalui dewan pakar yang dibentuk oleh pembina, Agus Saputro, Reza Ahmad Oktarian dkk telah mampu menyusun satu buku saku yang berisi kumpulan pukulan hadroh dari berbagai versi dan variasinya. Hal ini tentu merupakan satu perkembangan terbaru, di mana selama lebih dari 5 tahun perjalanan majelis - rujukan pukulan gendang yang selalu digunakan hanyalah berupa lembaran kertas fotocopyan hasil susunan M. Yunus dan M. Firdaus dkk di tahun 2012/2013. Disamping itu pula, pembina telah pula dimudahkan untuk menyusun satu risalah yang bernama ‘Risalah Rasikh’. Risalah ini berisi amalan yang ditujukan kepada segenap anggota dan muhibbin mejelis. Adapun isinya adalah: Surah Yasin, Surah Al-mulk, Ratibul Haddad, Wird Sakron, Surah Al Kahfi 1-10, serta maulid Adh-dhyaullami. Pengamalan wirid ini bisa berjamaah, pribadi, maupun khusus pada momen hajat tertentu. 
                    <br><br>
                    Adapun nama-nama yang berperan aktif mengembang majelis di masa kepengurusan ini antara lain: M. Firmansyah, M. Ilham Agung, Ahmadi Rahman Agus, Amat hariadi, Danil Irwansyah, M. Rafly Al Kasyaf, Ihwal Pat Pratama, Muhammad Ikhsan, M. Sidiq Jalu, Ahmad Alfid Rifa’I, Fahril Syahrul, Hermansyah, M. Fikri Rizal, Arjuna Aprinaldi, M. Habib Miftah Al Basyari, Mahsyuryadi, dll. Sementra untuk perempuannya ada nama; Nurhikmah, Nuraini, Indah Febria, Zulfa Agustina, Amira Adelia, Rossa Septiani, Nurfitria Syakira, Nirwana, Wahda Nurjannah, Yulia Ramadhani, Isma Puspita, Purwanti, Siti Ramadhanti, Elda, Radiah, Fadhilah Huzaimah dll.   
                    <br><br>
                    Terbentuknya Muhibbin di masa kepengurusan ini juga telah menunjukkan andilnya yang nyata dalam dakwah sholawat di Tanah Merah dan Kuala Enok. Pada mas itu dikenallah beberapa figur utama muhibbin yakni; Andi Yoga Pratama, Rafly Hamid, Ariyanto, M. Kaspul Amin (Senior di masa MM sekolah), Zilzian Atala Naufal, Rahmat Asyura, Ahmad Rifa’I, Dzul Fadhli, Syaikhul Kabir, Maulana, Suhendri Prayoga, M. Julfani (Dedek), Khairul Akbar, Fahmi Rizal, Wahyu, Jamal Mahsyur, dll. Di masa awal, anggota Muhibbin tidak hanya dari kalangan laki-laki namun ada juga beberap figur dr kalangan perempuan. Namun, berjalan beberapa bulan keanggotaanya mulai pasif dan hingga kini justru terhenti. Namun, pada geraknya yang terkait keanggotaan dari MAN 2 INHIL, Muhibbin akhwat (perempuan) tetap eksis dengan segala keterbatasannya. Hal ini mungkin saja juga terkait erat dengan berakhirnya masa kepengurusan pimpan M. Firmansyah, usai musyawarah ke-5 MM.
                    <br><br>
                    Pada Muber ke-5 majelis Mahabbaturrasul, terpilihlah Roshan Suhaimi Yahya sebagai ketua baru majelis. Di masa kepengurusan ini tak banyak program utama yang bisa teralisasi. Berselang beberapa bulan berjalan kepengurusan, aktifitas kegiatan latihan dan kajian mesti dihentikan sebab mewabahnya pendemi covid-19. Lebih dari 6 bulan majelis mengalami vakum kegiatan. Lalu, atas inisitif pembina dan masukan dari para senior pada bulan September 2020 wirid pembacaan maulid dan sholawat mulai dibuka kembali hingga kini. Sementara kajian umum di masjid besar Nurul Jamaah hingga kini belum bisa terlaksana, sebab mengingat terhubunganya aturan pembelajaran daring kepada para pelajar yang mana sebagian besar anggota jamaah kajian majelis mahabbaturrasul adalah pelajar. 
                    <br><br>
                    Berjalan beberapa bulan pasca dimulainya wirid dan latihan, gerak anggota dari kalangan madrasah mulai dirasa berkurang. Jika tidak ada suport oleh para muhibbin dan alumni MAN yang masih berada di seputaran Tanah Merah dan Kuala Enok, niscaya kevakuman dampak covid-19 ini bakalan mengoncangkan kaderisasi dan regenerasi majelis. Turun naiknya semangat dan pengorbanan perjuangan dalam dakwah adalah suatu hal lumrah terjadi dari zaman ke zaman. Hanya saja, penting untuk terus mengintrospeksi diri dan mencarikan jalan penyelesaian yang tepat. 
                    <br><br>
                    Beberapa figur yang banyak berkhidmad di masa kepengurasan ini antara lain dari kalangan laki-laki adalah: Roshan Suhaimi Yahya, Khairul Ikhsan, Rafli Faudi Nur, Bambang Setiawan, Ahmad Burhan Fadhil, Saharuddin, M. Lutfhi Al Mughni, M. Qusar Azriel, Rizki Isra Maulana, dll. Sementara di kalangan perempuan ada nama: Amanda Azhari, Rahma Novalia, Nurul Aulia, Nualifa Sadila, Nurhafni Awaliyah, Hikmatunnisa, Futri Nabila, Nur Ainun, Difa Aulia, Yohana Putri, Salmiyah Oktariani, Rumiya Ningsih, dll.  
                    </p>
