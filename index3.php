                    <br><br>
                    Di fase-fase masa awal, latihan ini hanya berupa sharing pengalaman dan wawasan oleh beberapa orang anggota yang sudah lebih dulu mengenal akan gendang hadroh ini dari pendahulu mereka. Adapun figur pendahulu tersebut telah menamatkan sekolahnya, ketika program latihan terbimbing ini mulai berjalan.
