                    <br><br>Selama lebih kurang setengah tahun bergerak dengan menggiatkan latihan serta ikut tampil di dalam momen-momen syiar ke Islaman di madrasah, Majelis Mahabbaturrasul mulai diminati dan diterima dengan tangan terbuka oleh keluarga besar MAN Kuala Enok. Lebih lanjut lagi dengan dipercayakan mengikuti berbagai lomba sholawat dan berpartisipasi dalam berbagai momen keislaman di luar madrasah. 
                    <br><br>Majelis Mahabbaturrasul secara resmi terbentuk tepat pada malam 27 Rajab tahun 1435 H/ 2014 M. Tanggal terbentuknya ini berdasarkan hasil musyawarah pertama yang juga menentukan beberapa kesepakatan pokok, diantaranya nama majelis, pembina mejelis, dan pengurus majelis. Pada musyawarah sederhana tersebut ditetapkanlah nama mejelis ialah Majelis Mahabbaturrasul. 
                    <br><br>Nama ini berawal dari usulan pembina majelis yang terinspirasi oleh satu tulisan di pojok Risalah kumpulan sholawat yang dalam beberapa momen sempat dibawakan oleh grup maulid dan sholawat yang dibina oleh KH. Muhammad Ainani. Di tepi bawah risalah tersebut tertera ‘Mahabaturrasul’ dengan bertuliskan arab. Di masa itu, KH. Muhammad Ainani memang banyak mengisi pengajian di berbagai masjid dan musholla di desa Tanah Merah dan Kel. Kuala Enok. Gendre sholawat yang dibawakan itu juga kerap dijadikan sebagai inspirasi untuk latihan dan penampilan. Selanjutnya pada musyawarah tersebut juga ditetapkan bapak Mohd Rofly Yahdillah sebagai pembina majelis. Adapun ketua pertama terpilihlah M. Amir dan beberapa orang pengurus inti lainnya.  </p>
