                    <br><br>
                    Di era semangat berislam yang makin meningkat, hadirnya majelis maulid dan sholawat merupakan suatu hal yang berjalan selaras dengan syiar dan trennya yang makin meluas di masyarakat. Majelis maulid dan sholawat di tengah masyarakat muslim masa kini telah membawa angin segar bagi kedekatan dan keterlibatan ummat, wabil khusus generasi muda Islam. Sebagai pengikut Islam terbesar di dunia, Indonesia adalah pengikut sunni yang kental. Hal tersebut berbanding lurus pula dengan perkembangan majelis maulidnya. Tradisi peringatan maulidan di tengah kaum sunni di Indonesia selama beratus tahun telah mengilhami terbentuknya berbagai majelis-majelis maulid. Dalam kurun lebih dari empat dekade sejak masa orde baru, majelis maulid dan sholawat tumbuh di mana-mana. Masyarakat muslim nusantara yang makin gandrung akan junjungannya - Rasulullah Saw terasa betah berlama-lama duduk dalam mejelis maulid. Mereka merasakan ketenangan bathin serta kekuatan ruhani sepulang dari menghadirinya.
                    <br><br>
                    Di tahun 80an ada beberapa majelis yang cukup digandrungi masyarakat akan eksistensi majelis maulid dan sholawatnya. Salah satu yang menjadi pioner di masa itu adalah, Majelis Maulid Guru Sekumpul di Martapura Kalimantan Selatan. Perkembangan majelis maulid dan sholawat makin membumi di Indonesia pasca lahirnya reformasi tahun 1998. Sejak 98 beberapa majelis maulid dan sholawat seperti Majelis Rasulullah Saw (Habib Munzir Al Musawa-Jakarta), Majelis Ahbabul Mustofa (Habib Syekh bin Abdul Qadir As-Seggaf-Solo) dan Majelis Nurul Musthofa (Habib Hasan bin Ja’far As-segaf-Jakarta) telah menjadi penggerak kegandrungan masyarakat muslim Indonesia akan maulid dan sholawat. 
                    <br><br>
                    Kehadiran majelis maulid dan sholawat secara nyata telah banyak memberi pengaruh positif bagi generasi muda. Para pelantun sholawat (munsyid), para pembaca rawi dan penabuh gendang umumnya adalah dari kalangan pemuda. Hal ini telah menjangkiti semua lini masyarakat muda, tidak hanya oleh kalangan pesantren atau anak sarungan semata. Di era tahun 2000an ke atas, peran majelis Hadroh Az-Zahir-Kanzul Sholawat dan Syubanul Muslimin telah mampu membawa puncak kemajuan syiar maulid dan sholawat di tanah air kita Indonesia, terlebih bagi kaula mudanya. 
                    <br><br>Majelis Mahabbaturrasul merupakan salah satu majelis maulid dan sholawat yang awal berdirinya tak bisa terpisahkan oleh peran MAN Kuala Enok yang berada di Kecamatan Tanah Merah, Kabupaten Indragiri Hilir, Provinsi Riau. Kehadiran majelis maulid dan sholawat Mahabbaturrasul juga merupakan salah satu bentuk jasa dan kiprah para pendahulu di medan dakwah yang satu ini. Maka, perlu kiranya menerangkan mengenai sejarah ringkas terbentuknya majelis ini. Diharapkan dengannya dapat menjadi satu catatan sejarah yang berguna di masa mendatang.
