             <h2 style="text-align: center;">Sejarah Perkembangan Majelis Mahabbaturrasul</h2>
                  <img src="assets/img/4.jpg" style="width: 100%">
                    <br><br>
                    <p class="text-justify">Pasca terbentuk, perjuangan dakwah majelis Mahabbaturrasul tidaklah sepi dari berbagai tantangan dan hambatan. Mulai dari patah tumbuh hilang bergantinya anggota disebabkan tamat madrasah dan hadirnya siswa baru, hingga masalah klasik mundurnya anggota akibat tergerus seleksi alam dakwah yang betul-betul berat bagi sebagian pemuda. 
                    <br><br>
                    Setelah berjalan lebih kurang 1 tahun, majelis makin eksis dan fokus pada beberapa agenda inti. Hal itu di antranya ialah berupa latihan gendang dan sholawat bersama yang terkoordinir secara rutin di madrasah maupun di rumah kediaman pembina. Wujud program yang juga jadi perhatian ialah melatih anggota yang baru bergabung di majelis. Sejak pengijazahan maulid, pembina banyak belajar dan berlatih sendiri serta kemudian menetapkan pembaca maulid adh-dhyaullami tiap pekan sebagai wiridan tetap semua anggota majelis. Di masa itu program majelis juga berjalan sinergi dengan program yasinan yang ditaja oleh Osis.
                    <br><br>
                    Maka, tak jarang anggota majelis dimintakan menyenandungkan sholawat dengan tabuhan hadrohnya. Bahkan, pada momen-momen tertentu yasinan juga diselingi dengan pembacaan maulid. Hal ini tentu semakin memperkokoh serta memberi peluang besar dikenalnya majelis, sebab program yasinan osis tersebut diadakan bergiliran oleh anggotanya dari rumah ke rumah.
                    <br><br>
                    Menjelang pergantian kepengurusan di masa estafet pertama majelis, beberapa orang penggerak awal telah menjadi alumni. Sementara yang senior sebagian juga telah berada di tingkat kelas teratas. Hal ini memunculkan satu konsekuensi tersendiri bagi arah gerak majelis sebab pada awal terbentuknya anggota majelis memang dari lintas angkatan 3 tahun. Semua anggota tersebut sama-sama berstatus pengurus generasi pemula dalam sudut pandang keanggotaannya dalam majelis.
                    <br><br>
                    Usai berjalan setahun kepengurusan, majelis pun melangsungkan kembali musyawarah besar dalam rangka pemilihan pengurus baru. Di era tahun ke-2 berdasarkan hasil musyawarah terpilihlah Dzul Fadhli Riva’I sebagai ketua majelis. Masa kepengurusan tahun ke-2 ini merupakan pengurus yang berada di tingkat kelas XI. Adapun figur-figur penggeraknya antara lain: Agus Saputro, Reza Ahmad Oktarian, Aprizal Amri, Zilzian Atala Naufal, Rahmat Ikhsan, Syaikhul Kabir, Abdurrahman, Rahmat Asyura dll. Pentolan utama masa ini dikenal akrab dengan nama slogan ‘wali songo’.
                    <br><br>
                    Di masa kepenguran ini, anggota majelis dari perempuan sudah mulai dibuka dan diterima, namun masih bersatu dengan kepengurusan laki-laki. Beberapa anggota dari kaum hawa yang cukup eksis dan berjasa mengembangkan majelis di masa ini antara lain: Farida, Nurani, Nur Fajri, Siti Aisyah, Amelia Febriani, Siti Zuhaira, Rida Agustina, Elmira, Nurkhalifah dll. Salah satu perkembangan majelis yang cukup menggembirakan di masa ini adalah dimulainya kajian pekanan yang berlangsung di masjid besar Nurul Jamaah bersama Ustadz Achmad Arfan. Kajian pekanan ini didahului oleh agenda pembacaan maulid Adh-dhyaullami dan sholawat yang disertai tabuhan gendang. Di masa awal, kajian ini hanya melibatkan pelajar dari MAN Kuala Enok.
                    <br><br>
                    Namun, pada perkembangan selanjutnya karena dilaksanakan di tengah masyarakat animo pemuda dan pelajar dari sekolah lain pun turut serta meramaikannya. Kajian ini bernuansa kajian turast yakni dengan pembacaan kitab-kitab ulama Aswaja. Bebera kitab yang pernah dikaji antara lain yang berjudul: Tijan Darori (Tauhid), Ta’limul Mutaalim (Adab dan Tasauf), Riyadhul Badiah (Tauhid dan Fiqh), dan Tafsir Surah Al Fatihah (Tafsir Al qur’an). Kajian ini diadakan setiap Selasa malam (malam Rabu) 
                    <br><br>
                    Peran Agus Saputro sebagai munsyid yang juga menguasai gendang di kalangan laki-laki saat itu juga tak bisa terpisahkan oleh figur M. Fadli (anak pak H. Ihsan Imran) dan Syafaruddin. Dua figur dari kalangan pemuda masyarakat umum ini telah banyak memberi kesan dan sharing pengalaman/ilmu bagi perkembangan majelis Mahabbaturrasul. Pada suatu kesempatan Agus Saputro dan M. Fadli diberi kepercayaan dari utusan kecamatan Tanah Merah untuk mengikuti pelatihan sholawat, gendang dan maulid yang ditaja oleh Dispora INHIL yang instrukturnya langsung dari Majelis Ahbabul Mustofa Indragiri Hilir.
                    <br><br>
                    Pasca itu, banyak skill baru gendang dan sholawat yang dapat diterapkan secara berangsur bagi anggota majelis. Di samping itu pula pembauran pengalaman Syafaruddin terhadap majelis yang sering beliau tunjukkan ketika latihan dan bergabung menjadi munsyid di kajian-kajian dan undangan telah mampu juga menambah deretan wawasan dan ilmu. 
                    <br><br>
                    Hal lain yang juga terwujud di masa kepengurusan Zul Fadli dkk adalah perdana pelaksanaan open. Kegiatan ini adalah berupa lomba sholawat dengan tabuhan gendang yang disertai pembacaan maulid. Adapun peserta lomba terfokus hanya pada internal madrasah yakni dari pembentukan grup-grup yang anggotanya lintas kelas dan angkatan. Kegiatan ini diberi nama OPEN MM ke-1. Pada open MM ini terbentuklah beberapa grup dari laki-laki yaitu: Faiz Al Arkhan, Al Kahfi, Mubarazul Muslimin dan Ikhwanul Muslimin sementara dipihak perempuan lahir lah grup : Asy-Syifa, Riyadhatusholihin, Al Arsyi Muntaha dan Dhya’urrahman. OPEN MM ke-1 berjalan sukses walau apa adanya, namun telah mampu memberi satu sumbangsih nyata untuk regenerasi dan kaderisasi anggota di era berikutnya. 
                    <br><br>
                    Dari sisi syiar ke masyarakat, di era kepemimpinan ini juga telah mampu membuka peluang dikenalnya majelis di masyarakat luas lewat berbagai momen yang melibatkannya. Kegiatan tersebut diantranya ; memenuhi undangan penampilan sholawat di beberapa masjid dan musholla dalam peringatan maulid nabi SAW dan isra mi’raj, berperan dalam undangan peringatan haul para tokoh ulama, memenuhi undangan mengisi sholawat dari masyarakat luas dalam momen ; aqiqahan, walimatul ursy, walimatus safar, selamatan pindah rumah serta menyemarakkan pawai tahun baru islam (Muharram) dan pawai menyambut bulan suci ramadhan, maulid seribu telur yang menjadi icon kecamatan Tanah Merah, serta mengahadirkan gendang sholawat arak-arakan baik untuk pengantin, pawai hingga mengiring kafilah pada MTQ Kabupaten.
                    <br><br>
                    Kunjungan dan ziarah makam ulama juga telah teralisasi di masa ini. Yakni mengadakan ziarah ke makam Syekh Abdurrahman Siddiq Al banjari di kampung Hidayat-Sapat kec. Kuala Indragiri. Ziarah juga bertepatan dengan haul sang ulama kharismatik Indragiri Hilir ini. Dengan kunjungan religi itu diharapkan semua anggota majelis mampu meneladani perjuangan dakwah ulama serta mendapat bagian berkah dari kedekatan kepada mereka. Ziarah juga terlaksana pada milad majelis yang ditujukan ke makam Tuan Guru KH Abdurrahman bin H. Bakrie di Sungai Pinang Kec. Tanah Merah. Majelis Mahabbaturrasul juga kerap diundang mengisi pembacaan maulid dan sholawat di perhelatan haul Tuan guru Sungai Pinang tersebut. 
                    </p>
